     private void categoryTableMouseClicked(java.awt.event.MouseEvent evt) {                                           
         int row = categoryTable.getSelectedRow();
            if (row != -1) {
        int categoryId = (int) categoryTable.getValueAt(row, 0);
        String categoryName = (String) categoryTable.getValueAt(row, 1);
        String description = (String) categoryTable.getValueAt(row, 2);

        // Set values to the edit fields
        nameField.setText(categoryName);
        descriptionField.setText(description);

        // Save the selected category ID for future use
        selectedCategoryId = categoryId;
    }
}
    
    
    
    private void loadRoomCategories() {
        tableModel.setRowCount(0); // Clear the existing table data
        ArrayList<RoomCategory> roomCategories = (ArrayList<RoomCategory>) roomCategoryService.getAllRoomCategories();

        for (RoomCategory roomCategory : roomCategories) {
            Object[] rowData = {
                    roomCategory.getCategoryId(),
                    roomCategory.getCategoryName(),
                    roomCategory.getDescription(),
                    "Delete"
            };
            tableModel.addRow(rowData);
        }
        
         categoryTable.getColumnModel().getColumn(0).setMaxWidth(0);
    categoryTable.getColumnModel().getColumn(0).setMinWidth(0);
    categoryTable.getColumnModel().getColumn(0).setPreferredWidth(0);
    categoryTable.getColumnModel().getColumn(0).setResizable(false);
    }
    
      
    
    
    
