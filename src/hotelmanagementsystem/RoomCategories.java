    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String categoryName = nameField.getText();
    String categoryDescription = descriptionField.getText();

    // Validate fields
    if (categoryName.isEmpty() || categoryDescription.isEmpty()) {
       
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }
    RoomCategoryService roomCategoryService = new RoomCategoryService();
    RandomNumberGenerator randm = new RandomNumberGenerator();
    // Create an instance of RoomCategory
    
    // Create an instance of RoomCategoryService
    if(selectedCategoryId != -1){
        RoomCategory roomCategory = new RoomCategory(selectedCategoryId,  categoryName, categoryDescription);
        roomCategoryService.updateRoomCategory(roomCategory);
          JOptionPane.showMessageDialog(this, "Category updated.", "Success", JOptionPane.DEFAULT_OPTION);
           nameField.setText("");
    descriptionField.setText("");
    selectedCategoryId =-1;
          
    }else{
       
   RoomCategory roomCategory = new RoomCategory(randm.generate(),  categoryName, categoryDescription);

    // Add the room category
    roomCategoryService.addRoomCategory(roomCategory);
    
    nameField.setText("");
    descriptionField.setText("");
    JOptionPane.showMessageDialog(this, "Category created.", "Success", JOptionPane.DEFAULT_OPTION);
    }
    loadRoomCategories();
    }//GEN-LAST:event_submitButtonActionPerformed

